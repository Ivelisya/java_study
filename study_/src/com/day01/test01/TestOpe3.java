public class TestOpe3 {
    public static void main(String[] args) {
        //返回Boolean值 要么是true 要么是false
        /*
                System.out.println(5 == 6);
                System.out.println(5 == 5);
         */


        //逻辑运算符 逻辑运算符左右连接的是布尔值
        System.out.println(true && false); //与运算符
        System.out.println(true || false); //或运算符
        System.out.println(!true); //非运算符

        //&& 如果两个操作数都是true 那么结果才是true
        //|| 如果两个操作数都是false 那么结果才是false
        //! 取反运算符 只有一个操作数
    }
}
